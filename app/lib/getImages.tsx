// function
export default function getImages(index?: number): ImageCollection[][] | ImageCollection[] {
	// Return a single campaign if index is specified, else return entire collection
	const payload: ImageCollection[][] | ImageCollection[] = index
		? (campaigns[index] as ImageCollection[])
		: (campaigns as ImageCollection[][]);

	if (!payload) throw new Error("Payload undefined.");

	return payload;
}
